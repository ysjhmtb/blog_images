const parseCookies = (cookie = '') =>
    cookie.split(';').map(v => v.split('=')).map(([k, ...vs]) => [k, vs.join('=')])
        .reduce((acc, [k,v]) => {
            acc[k.trim()] = decodeURIComponent(v);
            return acc;
        }, {});

console.log(parseCookies('name=zerocho;year=1994'));

